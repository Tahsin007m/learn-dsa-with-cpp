#include<iostream>
using namespace std;

int main(){
    
    int a = 10, b = 5;

    cout << "sum is = " << a+b << endl;
    cout << "difference is = " << a-b << endl;
    cout << "product is = " << a*b << endl;
    cout << "division is = " << a/b << endl;
    cout << "modulus is = " << a%b << endl;

    cout<<(5>3)<<endl; // true->1
    cout<<(5<3)<<endl; // false->0
    cout<<(5>=3)<<endl; // true->1
    cout<<(0==0.1)<<endl; // true->0
    
    return 0;
}
